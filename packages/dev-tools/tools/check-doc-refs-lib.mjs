// Pure functions for the doc dead-reference drift gate.
//
// Extracts backtick-enclosed repo paths from markdown content and classifies
// them as checkable (should exist on disk) or skippable (glob, template,
// illustrative, or explicitly allowlisted).
//
// No IO — all functions are unit-testable in isolation.
// The thin IO + CLI driver lives in check-doc-refs.mjs.

/** Repo-relative path prefixes that indicate a ref should be checked. */
export const REPO_PREFIXES = ['packages/', 'docs/', '.github/', '.agents/'];

/**
 * Built-in allowlist — paths (or glob patterns) that are valid doc references
 * but intentionally do not exist in the repo tree.
 *
 * Entries are matched as glob patterns relative to the repo root.
 */
export const BUILTIN_ALLOWLIST = [
  // External Microsoft Playwright repo path, not part of this monorepo.
  // docs/playwright-cli-sync.md links it with a github.com anchor.
  'packages/playwright-core/**',

  // Swift build artifact — generated under .build/, never committed.
  'packages/swift-server/.build/**',

  // Build-generated, .gitignored file regenerated by
  // packages/cherry/scripts/build-preview-bootstrap.mjs during postinstall
  // and the root build chain. Referenced by packages/cherry/CLAUDE.md.
  // Add similar entries here for any other gitignored build artifact that
  // docs legitimately reference.
  'packages/cloudflare-worker/src/preview-bridge-assets.ts',

  // Hypothetical future package mentioned conditionally in shared-ts/CLAUDE.md.
  'packages/shared-swift',

  // Hypothetical suggested refactor target mentioned in docs/pitfalls.md.
  'packages/webapp/src/shared',

  // Planned files from spec/plan docs (not yet created).
  'packages/webapp/src/kernel/realm/**',
  'packages/webapp/tests/kernel/realm/**',

  // Branch-only planning artifacts: docs/superpowers/specs/ and
  // docs/superpowers/plans/ are intentionally absent from main —
  // the planning-artifact-cleanup workflow scrubs them on merge.
  // docs/CLAUDE.md documents this explicitly.
  'docs/superpowers/**',
];

/**
 * Strip a raw backtick token to a clean repo-relative path:
 *   1. Split on the first whitespace (drops CLI args like --gate=x, §4 anchors, etc.)
 *   2. Strip any trailing `:...` suffix that has no following `/`
 *      (handles :42 line numbers, :functionName, :main() function refs).
 *   3. Strip a trailing `/` (the caller records whether it was present to
 *      optionally enforce directory-ness; this function just normalises).
 *
 * Returns `{ path, hadTrailingSlash }`.
 */
export function resolveToken(raw) {
  const firstWord = raw.split(/\s/)[0] ?? raw;
  // Strip colon suffix only when no slash follows the colon, e.g.
  //   file.ts:42          → file.ts
  //   file.ts:funcName    → file.ts
  //   packages/a:b/c.ts  → left intact (colon in mid-path is unusual but safe)
  const withoutColon = firstWord.replace(/:([^/]*)$/, '');
  const hadTrailingSlash = withoutColon.endsWith('/');
  const path = withoutColon.replace(/\/$/, '');
  return { path, hadTrailingSlash };
}

/** True when the path starts with an absolute separator — VFS / OS runtime paths. */
export function isAbsolutePath(path) {
  return path.startsWith('/');
}

/** True when the path starts with one of the known repo-relative prefixes. */
export function hasKnownPrefix(path) {
  return REPO_PREFIXES.some((pfx) => path.startsWith(pfx));
}

/** True when the path contains glob metacharacters (`*` or `{`). */
export function isGlobPath(path) {
  return path.includes('*') || path.includes('{');
}

/** True when the path contains an angle-bracket template placeholder, e.g. `<name>`. */
export function hasTemplatePlaceholder(path) {
  return path.includes('<');
}

/**
 * True when any path segment starts with `my-` — conventional illustrative
 * example names like `my-panel.ts`, `my-tool.ts`, `my-skill/`.
 */
export function isIllustrativePath(path) {
  return path.split('/').some((seg) => seg.startsWith('my-'));
}

/**
 * Convert a glob pattern to an anchored RegExp.
 * Supports `**` (zero or more segments), `*` (any chars in one segment), `?`.
 * Other regex metacharacters are escaped.
 */
export function globToRegex(glob) {
  let re = '';
  for (let i = 0; i < glob.length; i++) {
    const c = glob[i];
    if (c === '*' && glob[i + 1] === '*') {
      if (glob[i + 2] === '/') {
        re += '(?:.*/)?';
        i += 2;
      } else {
        re += '.*';
        i += 1;
      }
    } else if (c === '*') {
      re += '[^/]*';
    } else if (c === '?') {
      re += '[^/]';
    } else if ('.+^$(){}|[]\\'.includes(c)) {
      re += `\\${c}`;
    } else {
      re += c;
    }
  }
  return new RegExp(`^${re}$`);
}

/**
 * True when `path` matches at least one entry in `allowlist` (glob or exact).
 */
export function isAllowlisted(path, allowlist) {
  return allowlist.some((pattern) => pattern === path || globToRegex(pattern).test(path));
}

/**
 * Whether a path should be skipped without an existence check:
 *   - absolute path (VFS / OS runtime paths)
 *   - no known repo prefix
 *   - contains glob metacharacters
 *   - contains template placeholders like `<name>`
 *   - conventional illustrative `my-*` path
 *   - matches the built-in or caller-supplied allowlist
 *
 * @param {string} path  Cleaned repo-relative path (no trailing slash).
 * @param {string[]} extraAllowlist  Additional allowlist entries (merged with BUILTIN_ALLOWLIST).
 */
export function shouldSkip(path, extraAllowlist = []) {
  if (isAbsolutePath(path)) return true;
  if (!hasKnownPrefix(path)) return true;
  if (isGlobPath(path)) return true;
  if (hasTemplatePlaceholder(path)) return true;
  if (isIllustrativePath(path)) return true;
  const fullAllowlist = [...BUILTIN_ALLOWLIST, ...extraAllowlist];
  if (isAllowlisted(path, fullAllowlist)) return true;
  return false;
}

/**
 * Extract every checkable repo path from markdown `content`.
 *
 * Returns a deduplicated array of `{ path, hadTrailingSlash }` objects,
 * one per unique resolved path, in first-seen order.
 *
 * Skips: absolute paths, non-repo-prefixed tokens, globs, template
 * placeholders, illustrative `my-*` paths, and allowlisted paths.
 *
 * @param {string} content  Markdown source text.
 * @param {string[]} [extraAllowlist]  Additional allowlist patterns.
 */
export function extractCandidates(content, extraAllowlist = []) {
  // Match single-backtick inline code spans (not triple-backtick fences).
  const backtickRe = /`([^`\n]+)`/g;
  const seen = new Set();
  const results = [];

  for (const match of content.matchAll(backtickRe)) {
    const { path, hadTrailingSlash } = resolveToken(match[1]);
    if (shouldSkip(path, extraAllowlist)) continue;
    if (seen.has(path)) continue;
    seen.add(path);
    results.push({ path, hadTrailingSlash });
  }

  return results;
}

#!/usr/bin/env node

/**
 * Build script for preview-bootstrap.ts → single IIFE bundle with html2canvas-pro inlined.
 * Generates packages/cloudflare-worker/src/preview-bridge-assets.ts.
 */

import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import * as esbuild from 'esbuild';

const __dirname = dirname(fileURLToPath(import.meta.url));
const packageRoot = join(__dirname, '..');
const repoRoot = join(packageRoot, '../..');

const entryPoint = join(packageRoot, 'src/preview-bootstrap.ts');
const outFile = join(packageRoot, 'dist/preview-bridge.js');
const assetFile = join(repoRoot, 'packages/cloudflare-worker/src/preview-bridge-assets.ts');

async function build() {
  console.log('[preview-bootstrap] Building bundle...');

  const result = await esbuild.build({
    entryPoints: [entryPoint],
    bundle: true,
    format: 'iife',
    splitting: false,
    platform: 'browser',
    target: 'es2020',
    minify: true,
    outfile: outFile,
    metafile: true,
    logLevel: 'info',
  });

  console.log(`[preview-bootstrap] Bundle written to ${outFile}`);

  // Get the size from the first output (metafile keys are relative paths)
  const outputKey = Object.keys(result.metafile.outputs)[0];
  const bundleSize = result.metafile.outputs[outputKey]?.bytes;
  if (bundleSize !== undefined) {
    console.log(`[preview-bootstrap] Bundle size: ${(bundleSize / 1024).toFixed(1)} KiB`);
  }

  // Read the bundle and generate the asset file
  const { readFileSync } = await import('node:fs');
  const bundleContent = readFileSync(outFile, 'utf-8');

  const assetContent = `/**
 * Generated by the @ai-ecoverse/cherry preview-bootstrap build.
 * DO NOT hand-edit — this file is build-generated.
 */
export const PREVIEW_BRIDGE_JS = ${JSON.stringify(bundleContent)};
`;

  writeFileSync(assetFile, assetContent, 'utf-8');
  console.log(`[preview-bootstrap] Asset file written to ${assetFile}`);
}

build().catch((err) => {
  console.error('[preview-bootstrap] Build failed:', err);
  process.exit(1);
});

#!/usr/bin/env node
// Generate Sliccstart/Generated/WebKitManifest.swift from the pinned
// playwright-core devDependency.
//
// We don't ship Playwright at runtime — we just resolve the WebKit revision
// and download URLs at build time from playwright-core/browsers.json and
// playwright-core/lib/server/registry/index.js. Renovate keeps the dependency
// fresh; the generated Swift constants flow through to Sliccstart.
//
// The runtime installer (WebKitManager.install) downloads the zip from one of
// the CDN mirrors, unzips it into ~/Library/Caches/ms-playwright/webkit-<rev>/
// (the same layout `npx playwright install webkit` produces), and chmods the
// binary. Existing detection logic in WebKitManager.detectWebKit() picks it up
// from there.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { createRequire } from 'node:module';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const launcherRoot = resolve(__dirname, '..');
const require = createRequire(import.meta.url);

// Resolve playwright-core package directory relative to swift-launcher's
// node_modules (workspace install). We use require.resolve so npm hoisting is
// transparent.
const playwrightCorePkg = require.resolve('playwright-core/package.json', {
  paths: [launcherRoot],
});
const playwrightCoreRoot = dirname(playwrightCorePkg);

const browsersJsonPath = resolve(playwrightCoreRoot, 'browsers.json');
const registryJsPath = resolve(playwrightCoreRoot, 'lib/server/registry/index.js');

const browsersJson = JSON.parse(readFileSync(browsersJsonPath, 'utf8'));
const webkit = browsersJson.browsers.find((b) => b.name === 'webkit');
if (!webkit) {
  throw new Error('webkit entry missing from playwright-core/browsers.json');
}

const playwrightCoreVersion = JSON.parse(readFileSync(playwrightCorePkg, 'utf8')).version;

const registrySource = readFileSync(registryJsPath, 'utf8');

// Extract PLAYWRIGHT_CDN_MIRRORS = [ "..." ]
const mirrorsMatch = registrySource.match(/PLAYWRIGHT_CDN_MIRRORS\s*=\s*\[([\s\S]*?)\];/);
if (!mirrorsMatch) {
  throw new Error('Could not locate PLAYWRIGHT_CDN_MIRRORS in registry/index.js');
}
const mirrors = [...mirrorsMatch[1].matchAll(/"([^"]+)"/g)].map((m) => m[1]);
if (mirrors.length === 0) {
  throw new Error('Found PLAYWRIGHT_CDN_MIRRORS but no URLs inside');
}

// Extract DOWNLOAD_PATHS first (there's also an EXECUTABLE_PATHS["webkit"]
// block earlier that has no mac paths), then locate the webkit subobject.
const downloadPathsMatch = registrySource.match(/const\s+DOWNLOAD_PATHS\s*=\s*\{([\s\S]*?)\n\};/);
if (!downloadPathsMatch) {
  throw new Error('Could not locate DOWNLOAD_PATHS in registry/index.js');
}
const downloadPathsBlock = downloadPathsMatch[1];

const webkitBlockMatch = downloadPathsBlock.match(/"webkit":\s*\{([\s\S]*?)\n\s*\},/);
if (!webkitBlockMatch) {
  throw new Error('Could not locate "webkit" block inside DOWNLOAD_PATHS');
}
const webkitBlock = webkitBlockMatch[1];

function extractPath(platformKey) {
  const re = new RegExp(`"${platformKey}":\\s*"([^"]+)"`);
  const match = webkitBlock.match(re);
  return match ? match[1] : null;
}

// We only ship Sliccstart for macOS — collect every defined mac path.
// `revision` is substituted for %s the same way Playwright does it.
const macPlatforms = [
  'mac11',
  'mac11-arm64',
  'mac12',
  'mac12-arm64',
  'mac13',
  'mac13-arm64',
  'mac14',
  'mac14-arm64',
  'mac15',
  'mac15-arm64',
];

const revision = String(webkit.revision);

function urlsForPlatform(platformKey) {
  const template = extractPath(platformKey);
  if (!template) {
    return [];
  }
  const path = template.replace('%s', revision);
  return mirrors.map((mirror) => `${mirror}/${path}`);
}

const platformUrls = Object.fromEntries(
  macPlatforms.map((p) => [p, urlsForPlatform(p)]).filter(([, urls]) => urls.length > 0)
);

if (Object.keys(platformUrls).length === 0) {
  throw new Error('No macOS WebKit download paths resolved from registry/index.js');
}

// Emit Swift source.
function swiftStringArrayLiteral(values, indent) {
  if (values.length === 0) return '[]';
  const lines = values.map((v) => `${indent}    "${v}",`).join('\n');
  return `[\n${lines}\n${indent}]`;
}

function swiftDictLiteral(map, indent) {
  const entries = Object.entries(map);
  if (entries.length === 0) return '[:]';
  const lines = entries
    .map(
      ([key, urls]) => `${indent}    "${key}": ${swiftStringArrayLiteral(urls, indent + '    ')}`
    )
    .join(',\n');
  return `[\n${lines}\n${indent}]`;
}

const generatedSwift = `// Generated by scripts/generate-webkit-manifest.mjs.
// DO NOT EDIT. Regenerated on every npm run build -w @slicc/swift-launcher
// from the pinned playwright-core devDependency.
//
// Source: playwright-core ${playwrightCoreVersion}
//   browsers.json: webkit revision ${revision}, browserVersion ${webkit.browserVersion}
//   registry/index.js: PLAYWRIGHT_CDN_MIRRORS + DOWNLOAD_PATHS["webkit"]
//
// Renovate bumps playwright-core in package.json; the next build regenerates
// this file with the new revision and any URL changes.

import Foundation

enum WebKitManifest {
    /// Playwright's WebKit revision number. Matches the directory name
    /// Playwright would create at ~/Library/Caches/ms-playwright/webkit-<rev>/.
    static let revision: String = "${revision}"

    /// Human-readable WebKit version (Safari version that Playwright tracks).
    static let browserVersion: String = "${webkit.browserVersion}"

    /// Source playwright-core npm version (for diagnostics).
    static let playwrightCoreVersion: String = "${playwrightCoreVersion}"

    /// Cache directory leaf name, e.g. "webkit-2248".
    static let installDirName: String = "webkit-${revision}"

    /// Full URLs for each macOS platform identifier Playwright supports.
    /// Keys match Playwright's hostPlatform.shortPlatform strings:
    ///   "mac<major>" for x86_64, "mac<major>-arm64" for Apple Silicon.
    /// Multiple URLs per platform represent CDN fallbacks; try them in order.
    static let downloadUrlsByPlatform: [String: [String]] = ${swiftDictLiteral(platformUrls, '    ')}
}
`;

const outDir = resolve(launcherRoot, 'Sliccstart/Generated');
mkdirSync(outDir, { recursive: true });
const outPath = resolve(outDir, 'WebKitManifest.swift');
writeFileSync(outPath, generatedSwift);

console.log(
  `Generated ${outPath}\n  webkit revision: ${revision}\n  playwright-core: ${playwrightCoreVersion}\n  platforms: ${Object.keys(platformUrls).join(', ')}`
);
